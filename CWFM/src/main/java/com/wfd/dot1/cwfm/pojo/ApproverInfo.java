package com.wfd.dot1.cwfm.pojo;

public class ApproverInfo {

	private String gatePassApproverInfoId;
	private String gatePassId;
	private String userRole;
	private int index;
	private int status;
	private String createdBy;
	private String createdDate;
	public String getGatePassApproverInfoId() {
		return gatePassApproverInfoId;
	}
	public void setGatePassApproverInfoId(String gatePassApproverInfoId) {
		this.gatePassApproverInfoId = gatePassApproverInfoId;
	}
	public String getGatePassId() {
		return gatePassId;
	}
	public void setGatePassId(String gatePassId) {
		this.gatePassId = gatePassId;
	}
	public String getUserRole() {
		return userRole;
	}
	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	
	
}
