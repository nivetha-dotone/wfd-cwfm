package com.wfd.dot1.cwfm.pojo;

public class PersonOrgLevel {

	private String levelDef;
	private String id;
	private String oleId;
	private String description;
	public String getLevelDef() {
		return levelDef;
	}
	public void setLevelDef(String levelDef) {
		this.levelDef = levelDef;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOleId() {
		return oleId;
	}
	public void setOleId(String oleId) {
		this.oleId = oleId;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
}
